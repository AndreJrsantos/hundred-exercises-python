#Conversor de Medidas
metro = float(input("Medida em Metros: "))
cm = (metro * 100)
mm = (metro * 1000)
print(f"-----------------------------------")
print(f"{metro} metros = {cm} centímetros.")
print(f"-----------------------------------")
print(f"{metro} metros = {mm} milímetros.")
print(f"-----------------------------------")
