#Dobro, Triplo e Raiz Quadrada
num = int(input("Digite um Número: "))
dobro = (num * 2)
triplo = (num * 3)
raiz = float(num ** (1/2))

print("-----------------------------")
print(f"O Número Digitado Foi: {num}")
print("-----------------------------")
print(f"O Dobro é = {dobro}")
print(f"O Triplo é = {triplo}")
print(f"A Raiz Quadrada é = {raiz:.2f}")
print("-----------------------------")